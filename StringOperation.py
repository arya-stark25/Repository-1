# How to perform various operations on Strings

# How to calculate the length of the String!!
Str1= "Rajkumar"
len1= len(Str1)  # Calculates the length of the String
print("Length of the String =", len1)

# How to convert Integer and floating points numbers into String
numb1= 10
StrA= str(numb1)
print("The converted string is=", StrA)
numb2= 12.34
StrB= str(numb2)
print("The converted string is=", StrB)

# How to covert all the string characters into lower case letters
Str1= "CHIEFJUSTICE"
Str2= Str1.lower()
print("The converted lower case string is=",Str2)

# How to covert all the string characters into upper case letters
Str1="chiefjustice"
Str2=Str1.upper()
print("The converted upper case string is=",Str2)




